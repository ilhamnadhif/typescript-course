
// Enum

// numeric enum
{
    enum Month {
        JAN = 1,
        FEB = 10,
        MAR = 6,
        APR,
        MAY
    }
    console.log(Month);
}

// string enum
{
    enum Month {
        JAN = "Januari",
        FEB = "Februari",
        MAR = "Maret",
        APR = "April",
        MAY = "Mei"
    }
    console.log(Month);
}

